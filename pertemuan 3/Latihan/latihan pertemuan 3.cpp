#include <iostream>
using namespace std;

int main(){
	int a = 5;
	
	cout<<"nilai awal a = "<<a << endl;
	
	cout<<" pre-increment -> ++a = " << ++a << endl;
	
	cout<< "post-increment -> a++ = " << a++ <<", sekarang a = " << a <<  endl;
	
	cout << "pre-decrement -> --a = " << --a << endl;
	
	cout << "post-decrement -> a-- = " << a-- << "sekarang a = " << a << endl;
	
	
}
